<h4>Zensur</h4>
Wir haben davon abgesehen unsere Domains zensurresistent per <a href="https://ens.domains" target="_blank">ens.domains</a> & IPFS bereitzustellen. 
Wir hoffen, dass wir trotz aller <a href="https://www.youtube.com/watch?v=oPZK3l93LyI" target="_blank">Staatskriminalität</a> der zurückliegenden Jahre auf legalem Weg Architekturen der Freiheit fördern können.
<p><br /><br /><br /></p>


<h4>Individuelle Freiheit</h4>
<p><br /></p>
Unsere Projekte sind als Spielräume für Freiheit, Weltfrieden und Freundschaft zwischen allen Menschen
aller Völker und Nationen zu begreifen.
<p><br /></p>
Zu diesem Zweck sind freiheitliche Währungen nicht regierungsspezifisch sondern individuell. <p><br></p> 
Jeder
<a href="https://www.perlentaucher.de/buch/ruediger-safranski/einzeln-sein.html" target="_blank"
	>Einzelne</a
>
sollte wahrscheinlich grundsätzlich seine eigene Währung gestalten können und frei entscheiden können
welche Währungen er wofür nutzen möchte.
<p><br /></p>
Da wir Respekt für alle Menschen überall auf der Welt haben, glauben wir, dass dies eine gute
Idee ist.

